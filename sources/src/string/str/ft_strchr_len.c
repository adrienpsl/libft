#include "../../../ft_library_header.h"

size_t	ft_strchr_len(char *s, char c)
{
	size_t i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (i);
}
