#include "libft.h"

char *ft_strsplit_search(char **split, int(*f)(char *, void *), void *param)
{
	if (!split || !*split)
		return (NULL);
	while (*split)
	{
		if (f(*split, param))
		    return (*split);
		split++;
	}
	return (NULL);
}

