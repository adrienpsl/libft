#include "libft.h"

int ft_capitalise(int c, int mode)
{
	if (mode && (c >= 'a' && c <= 'z'))
		return (c - 32);
	if (!mode && (c >= 'A' && c <= 'Z'))
		return (c + 32);
	return (c);
}