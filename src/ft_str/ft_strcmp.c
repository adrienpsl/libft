int ft_str_cmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	if (!(s1) || !(s2))
		return (-1);
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}