#include <ft_printf.h>

int	ft_strlen(const char *str)
{
	int i;

	i = 0;
	if (str == NULL)
	{
		ft_printf("ft_strlen: ptr null");
		return (0);
	}
	while (str[i])
		i++;
	return (i);
}
